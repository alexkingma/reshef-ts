export const spellHasTarget = (cardName: CardName) => {
  return (cardName as Spell) in targetedSpellMap;
};

export const isValidSpellTarget = (
  originSpell: CardName,
  targetMonster: CardName
) => {
  const validTargets = targetedSpellMap[originSpell as Spell];
  if (!validTargets) {
    console.log(`No spell targets found for card: ${originSpell}`);
    return false;
  }

  return validTargets.has(targetMonster as Monster);
};

export const isReflectablePowerUp = (cardName: CardName) => {
  // i.e. should it trigger Bad Reaction to Simochi
  return (
    spellHasTarget(cardName) &&
    ![
      // non-power-up effects
      Spell.ElegantEgotist,
      Spell.Metalmorph,
    ].includes(cardName as Spell)
  );
};
